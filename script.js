// Demonstrate Data Types
let FirstName = "Luanna" // String Value
console.log(FirstName)

let height = 167.64; // Number Value
console.log(height); 

let isHuman = true; // Boolean Value
console.log(isHuman)

let Pokemon = ['Turtwig', 'Snivy', 'Bulbasaur']; // Array Value
console.log(Pokemon); 

let birthDay = {
   month: "October",
   day: 16,
   year: 1997,
 }; // Object Value
console.log(birthDay); 

// Arithmetic Operators
console.log(32 + 5); 
console.log(4 - 1); 
console.log(6 * 9); 
console.log(8 / 2); 

// Comparison Operators
let x = 3
let y = 7
console.log(x > y); 
console.log(x < y); 
console.log (x === y); 
